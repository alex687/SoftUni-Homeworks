package dots;

public class Dots {
	private double x;
	private double y;

	public Dots(double x, double y) {
		this.setX(x);
		this.setY(y);
	}

	public Dots() {
	}

	public double getX() {
		return x;
	}

	public void setX(double x) {
		this.x = x;
	}

	public double getY() {
		return y;
	}

	public void setY(double y) {
		this.y = y;
	}
}
